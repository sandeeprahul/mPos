public class UserDetails {

    String userID;
    String storeID;
    String loyaltyID;
    String loyaltyPWD;
    String tillNo;
    String isPB;
    String storeEmail;
    String storePwd;
    String sendEmailID;

    public String getUserID() {
        return userID;
    }

    public void setUserID(String userID) {
        this.userID = userID;
    }

    public String getStoreID() {
        return storeID;
    }

    public void setStoreID(String storeID) {
        this.storeID = storeID;
    }

    public String getLoyaltyID() {
        return loyaltyID;
    }

    public void setLoyaltyID(String loyaltyID) {
        this.loyaltyID = loyaltyID;
    }

    public String getLoyaltyPWD() {
        return loyaltyPWD;
    }

    public void setLoyaltyPWD(String loyaltyPWD) {
        this.loyaltyPWD = loyaltyPWD;
    }

    public String getTillNo() {
        return tillNo;
    }

    public void setTillNo(String tillNo) {
        this.tillNo = tillNo;
    }

    public String getIsPB() {
        return isPB;
    }
    public void setIsPB(String isPB) {
        this.isPB = isPB;
    }

    public String getStoreEmail() {
        return storeEmail;
    }
    public void setStoreEmail(String storeEmail) {
        this.storeEmail = storeEmail;
    }

    public String getStorePwd() {
        return storePwd;
    }
    public void setStorePwd(String storePwd) {
        this.storePwd = storePwd;
    }

    public String getSendEmailID() {
        return sendEmailID;
    }
    public void setSendEmailID(String sendEmailID) {
        this.sendEmailID = sendEmailID;
    }


}
