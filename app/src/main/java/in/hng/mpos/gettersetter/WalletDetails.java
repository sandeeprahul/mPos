package in.hng.mpos.gettersetter;

public class WalletDetails {

    String walletID;
    String walletName;
    String walletAmount;
    String walletOTP;
    String WalletTransID;

    public String getWalletID() {
        return walletID;
    }
    public void setWalletID(String walletID) {
        this.walletID = walletID;
    }

    public String getWalletName() {
        return walletName;
    }
    public void setWalletName(String walletName) {
        this.walletName = walletName;
    }

    public String getWalletAmount() {
        return walletAmount;
    }
    public void setWalletAmount(String walletAmount) {
        this.walletAmount = walletAmount;
    }

    public String getWalletOTP() {
        return walletOTP;
    }
    public void setWalletOTP(String walletOTP) {
        this.walletOTP = walletOTP;
    }

    public String getWalletTransID() {
        return WalletTransID;
    }
    public void setWalletTransID(String WalletTransID) {
        this.WalletTransID = WalletTransID;
    }


}
